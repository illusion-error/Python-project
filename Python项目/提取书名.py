import re

book_info = {}
current_category = None
for line in text.split("\n"):
    if line.endswith("类"):
        current_category = line[:-1].lower()
        book_info[current_category] = []
    elif line.startswith("《"):
        match = re.match(r"《(.*?)》：.*。(.*)", line)
        if match:
            title = match.group(1)
            book_info[current_category].append(f"  - 《{title}》")

for category, books in book_info.items():
    print(f"{category}:")
    for book in books:
        print(book)
